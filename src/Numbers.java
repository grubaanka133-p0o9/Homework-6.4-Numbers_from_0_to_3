public class Numbers {
    public static void main(String[] args) {
        double a = 0;
        while (a<=30){
            System.out.print(a/10 +", ");
            a++;
        }
        double b = 0;
        do {
            System.out.print(b/10 +", ");
            b++;
        }while (b<=30);


    }
}